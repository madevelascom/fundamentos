						<h4 class="bold">Objetivo general</h3>
							<ul>
								<li>Desarrollar programas en un lenguaje de programación de alto nivel para resolver problemas relacionados a su profesión.</li>
							</ul>

							<h4 class="bold">Objetivos específicos</h4>
							<ul>
								<li>Aplicar métodos computacionales para resolver problemas de su campo de estudio utilizando un lenguaje de programación.</li>
								<li>Utilizar herramientas computacionales para modelar y entender datos.</li>
								<li>Aplicar algoritmos receta en la resolución de problemas.</li>
								<li>Utilizar un ambiente de desarrollo para escribir y depurar programas.</li>
								<li>Utilizar modularización para la simplificación de la estructura de un programa.</li>
							</ul>

						</div>
						<div role="tabpanel" class="tab-pane fade" id="syllabus">
							<p class = 'pt-10'>El contenido de la materia está distribuido de la siguiente manera</p>

							<h4 class="bold">Unidad 1: Introducción a la programación</h4>
							<ul>
								<li>Interpretadores y compiladores.</li>
								<li>Ambientes de programación.</li>
								<li>Conceptos y propiedades de los algoritmos.</li>
							</ul>
							<h4 class="bold">Unidad 2: Variables y tipos de datos</h4>
							<ul>
								<li>Tipos de datos, operadores lógicos y relacionales.</li>
								<li>Conversiones entre tipos de datos y manejo de entrada-salida.</li>
								<li>Operaciones con cadenas de caracteres.</li>
							</ul>
							<h4 class="bold">Unidad 3: Estructuras de control</h4>
							<ul>
								<li>Estructuras de control condicionales.</li>
								<li>Estructuras de control iterativas.</li>
								<li>Sentencias anidadas.</li>
							</ul>
							<h4 class="bold">Unidad 4: Listas</h4>
							<ul>
								<li>Listas y sus propiedades.</li>
								<li>Indexación, slicing y funciones básicas de listas.</li>
								<li>Manejo de caracteres como listas.</li>
							</ul>
							<h4 class="bold">Unidad 5: Arreglos N-Dimensionales</h4>
							<ul>
								<li>Propiedades de los arreglos.</li>
								<li>Indexación y slicing.</li>
								<li>Operaciones aritméticas, estadísticas y de ordenamiento.</li>
								<li>Broadcasting.</li>
							</ul>
							<h4 class="bold">Unidad 6: Funciones</h4>
							<ul>
								<li>Paradigma "divide y vencerás".</li>
								<li>Principios y conceptos fundamentales de diseño.</li>
								<li>Definición e implementación de funciones.</li>
								<li> Modularización.</li>
							</ul>
							<h4 class="bold">Unidad 7: Colecciones </h4>
							<ul>
								<li>Características de las colecciones.</li>
								<li>Tipos de colecciones.</li>
							</ul>
							<h4 class="bold">Unidad 8: Archivos - Entrada/Salida </h4>

							<ul>
								<li>Archivos y operaciones de entrada.</li>
								<li>Archivos y operaciones de salida.</li>
							</ul>
							<h4 class="bold">Unidad 9: Procesamiento de datos</h4>

							<ul>
								<li>Filtrado y agrupación de datos.</li>
								<li>Extracción y exportación de datos.</li>
								<li>Visualización básica de datos.</li>
							</ul>
